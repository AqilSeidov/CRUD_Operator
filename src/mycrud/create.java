            String name = txtname.getText();
            String lastname = txtlastname.getText();
            String uid = txtid.getText();
            String age = txtage.getText();
            String phone = txtphone.getText();
            String city = txtcity.getText();
            
            
            Class.forName("org.mariadb.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/crud","root", "");
            insert = con.prepareStatement("insert into mastercrud(name,lastname,identity,age,city,phone)values(?,?,?,?,?,?)");
            //insert.setString(1, dbid);
            insert.setString(1, name);
            insert.setString(2, lastname);
            insert.setString(3, uid);
            insert.setString(4, age);
            insert.setString(5, city);
            insert.setString(6, phone);

            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Record Added");
            
            txtname.setText("");
            txtlastname.setText("");
            txtid.setText("");
            txtcity.setText("");
            txtphone.setText("");
            txtage.setText("");
            txtname.requestFocus();
            
            
            
            
            
            
            
